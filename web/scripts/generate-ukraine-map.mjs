// Generates dotted Ukraine map data + projected city coordinates.
// Run once when map data needs regeneration:
//   node scripts/generate-ukraine-map.mjs
//
// Inputs:
//   - Ukraine GeoJSON (fetched from johan/world.geo.json)
// Outputs:
//   - src/lib/ukraine-dots.ts
//   - src/lib/bond-cities.ts

import fs from "node:fs";
import https from "node:https";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = path.resolve(__dirname, "..");

const GEOJSON_URL =
  "https://raw.githubusercontent.com/johan/world.geo.json/master/countries/UKR.geo.json";

const VIEWBOX_W = 1000;
const VIEWBOX_H = 600;
const DOT_SPACING = 14;
const DOT_JITTER = 0; // 0 = clean grid, >0 = organic look

const CITIES = [
  { id: "UA-UAH-SHORT", name: "Kyiv", lng: 30.5234, lat: 50.4501 },
  { id: "UA-UAH-MID", name: "Lviv", lng: 24.0297, lat: 49.8397 },
  { id: "UA-USD-STD", name: "Odesa", lng: 30.7233, lat: 46.4825 },
  { id: "UA-EUR-STD", name: "Uzhhorod", lng: 22.2879, lat: 48.6208 },
  { id: "UA-UAH-WAR", name: "Kharkiv", lng: 36.2304, lat: 49.9935 },
  { id: "UA-USD-WAR", name: "Dnipro", lng: 35.0462, lat: 48.4647 },
];

function fetchText(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if (res.statusCode && res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
          fetchText(res.headers.location).then(resolve, reject);
          return;
        }
        let body = "";
        res.on("data", (chunk) => (body += chunk));
        res.on("end", () => resolve(body));
      })
      .on("error", reject);
  });
}

function toMercatorX(lngDeg) {
  return (lngDeg * Math.PI) / 180;
}

function toMercatorY(latDeg) {
  return Math.log(Math.tan(Math.PI / 4 + (latDeg * Math.PI) / 360));
}

function pointInPolygon([x, y], polygon) {
  let inside = false;
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const [xi, yi] = polygon[i];
    const [xj, yj] = polygon[j];
    const intersect =
      yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi;
    if (intersect) inside = !inside;
  }
  return inside;
}

async function main() {
  console.log(`Fetching ${GEOJSON_URL}`);
  const raw = await fetchText(GEOJSON_URL);
  const geojson = JSON.parse(raw);
  const polygon = geojson.features[0].geometry.coordinates[0];

  // Compute bbox in Mercator space
  let minMX = Infinity,
    maxMX = -Infinity,
    minMY = Infinity,
    maxMY = -Infinity;
  for (const [lng, lat] of polygon) {
    const mx = toMercatorX(lng);
    const my = toMercatorY(lat);
    if (mx < minMX) minMX = mx;
    if (mx > maxMX) maxMX = mx;
    if (my < minMY) minMY = my;
    if (my > maxMY) maxMY = my;
  }
  const mxRange = maxMX - minMX;
  const myRange = maxMY - minMY;

  const scale = Math.min(VIEWBOX_W / mxRange, VIEWBOX_H / myRange);
  const mapW = mxRange * scale;
  const mapH = myRange * scale;
  const offsetX = (VIEWBOX_W - mapW) / 2;
  const offsetY = (VIEWBOX_H - mapH) / 2;

  function project(lng, lat) {
    const mx = toMercatorX(lng);
    const my = toMercatorY(lat);
    const x = offsetX + (mx - minMX) * scale;
    const y = offsetY + (maxMY - my) * scale; // flip Y for screen
    return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
  }

  const projectedPolygon = polygon.map(([lng, lat]) => project(lng, lat));

  // Generate dot grid, filter by polygon
  const dots = [];
  for (let y = 0; y <= VIEWBOX_H; y += DOT_SPACING) {
    const rowOffset = (Math.floor(y / DOT_SPACING) % 2) * (DOT_SPACING / 2);
    for (let x = 0; x <= VIEWBOX_W; x += DOT_SPACING) {
      const jitterX = DOT_JITTER ? (Math.random() - 0.5) * DOT_JITTER : 0;
      const jitterY = DOT_JITTER ? (Math.random() - 0.5) * DOT_JITTER : 0;
      const px = x + rowOffset + jitterX;
      const py = y + jitterY;
      if (pointInPolygon([px, py], projectedPolygon)) {
        dots.push([Math.round(px * 10) / 10, Math.round(py * 10) / 10]);
      }
    }
  }

  console.log(`Generated ${dots.length} dots inside Ukraine polygon`);

  const projectedCities = CITIES.map((c) => {
    const [x, y] = project(c.lng, c.lat);
    return { ...c, x, y };
  });

  const dotsOutput = `// Auto-generated by scripts/generate-ukraine-map.mjs. Do not edit manually.

export const UKRAINE_VIEWBOX = { width: ${VIEWBOX_W}, height: ${VIEWBOX_H} } as const;

export const UKRAINE_DOTS: readonly (readonly [number, number])[] = [
${dots.map(([x, y]) => `  [${x}, ${y}],`).join("\n")}
];
`;

  const citiesOutput = `// Auto-generated by scripts/generate-ukraine-map.mjs. Do not edit manually.

export interface BondCity {
  readonly id: string;
  readonly name: string;
  readonly lng: number;
  readonly lat: number;
  readonly x: number;
  readonly y: number;
}

export const BOND_CITIES: readonly BondCity[] = [
${projectedCities
  .map(
    (c) =>
      `  { id: "${c.id}", name: "${c.name}", lng: ${c.lng}, lat: ${c.lat}, x: ${c.x}, y: ${c.y} },`,
  )
  .join("\n")}
];

export function getBondCity(id: string): BondCity | undefined {
  return BOND_CITIES.find((c) => c.id === id);
}
`;

  fs.writeFileSync(path.join(WEB_ROOT, "src/lib/ukraine-dots.ts"), dotsOutput);
  fs.writeFileSync(path.join(WEB_ROOT, "src/lib/bond-cities.ts"), citiesOutput);

  console.log("Wrote src/lib/ukraine-dots.ts");
  console.log("Wrote src/lib/bond-cities.ts");
  console.log(`Cities projected:`);
  for (const c of projectedCities) {
    console.log(`  ${c.name.padEnd(10)} → (${c.x}, ${c.y})`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
